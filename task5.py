""" Compute the sum and product of two numbers input by the user, displaying the results in a single print() statement using the sep argument."""
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))
sum_result = num1 + num2
product_result = num1 * num2

print("Sum:", sum_result, "Product:", product_result, sep=" | ")


